package com.rhinoforms;

@SuppressWarnings("serial")
public class DocumentManipulatorException extends Exception {

	public DocumentManipulatorException(String message) {
		super(message);
	}

	public DocumentManipulatorException(String message, Throwable cause) {
		super(message, cause);
	}

	public DocumentManipulatorException(Throwable cause) {
		super(cause);
	}

}
