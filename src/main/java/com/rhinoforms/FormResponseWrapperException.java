package com.rhinoforms;

public class FormResponseWrapperException extends Exception {

	private static final long serialVersionUID = -4825055974587257610L;

	public FormResponseWrapperException(String message, Throwable cause) {
		super(message, cause);
	}

	public FormResponseWrapperException(String message) {
		super(message);
	}

	public FormResponseWrapperException(Throwable cause) {
		super(cause);
	}

}
